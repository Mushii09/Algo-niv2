public class count20to0 {
    public static void main(String[] args) {
        //On déclare la varible entiers i à 0
        int i = 30;
        
        // Tant que la variable n'est pas inférieur ou égale à 10
        
        while (i >= 0){
        System.out.print(i + " ");
        i--;
        System.out.println();
        }
        System.out.println();

    }
}
